void show_temperature_handler(void)
{
	MAX7219_set_brightness(15);
	get_Temperature();
	display_buffer_int = Temp[0];

}
void adjust_temperature_handler(void)
{

  uwTick = 0;
	while (HAL_GetTick() < 3000)
	{
		MAX7219_set_brightness(0);
		display_buffer_int = target_temperature;
		if (btn1_pressed_flag)
		{
			btn1_pressed_flag = false;
			target_temperature -= 1;
			uwTick = 0;
		}

		if (btn2_pressed_flag)
		{
			btn2_pressed_flag = false;
			target_temperature += 1;
			uwTick = 0;
		}
		MAX7219_print_temperature(target_temperature);
	}

	program_state = SHOW_TEMPERATURE;
}
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	currentMillis = HAL_GetTick();
	if (GPIO_Pin == BTN1_Pin && (currentMillis - previousMillis > 150))
	{
		btn1_pressed_flag = true;
		previousMillis = currentMillis;
	}
	if (GPIO_Pin == BTN2_Pin && (currentMillis - previousMillis > 150))
	{
		btn2_pressed_flag = true;
		previousMillis = currentMillis;
	}

}


